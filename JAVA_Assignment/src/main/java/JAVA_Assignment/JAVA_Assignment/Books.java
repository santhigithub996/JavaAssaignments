package JAVA_Assignment.JAVA_Assignment;

public class Books {
	
	String bookname;
	float bookprice;
	
	public void setname(String name)
	{
		bookname=name;
	}
	public void setprice(float price)
	{
		bookprice=price;
	}
	public String getname()
	{
		return bookname;
	}
	public float getprice()
	{
		return bookprice;
	}

}
